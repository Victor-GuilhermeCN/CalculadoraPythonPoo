class BasicOperations:
    def __init__(self, n1, n2):
        self.n1 = n1
        self.n2 = n2

    def some(self, n1, n2):
        return n1 + n2

    def subtr(self, n1, n2):
        return n1 - n2

    def mult(self, n1, n2):
        return n1 * n2

    def division(self, n1, n2):
        return n1 / n2